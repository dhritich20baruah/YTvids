#Variables can store data of different types, and different types can do different things. Python has the following data types built-in by default.
p = "hello" #str
p = 20 #int
p = 12.3 #float
p = 1j #complex
p = ["pen", "paper", "pencil"] #list
p = (1, 2, 3) #tuple
p = range(3) #range
p = {"name": "Dhriti", "title": "Baruah"} #dict
p = {"milk", "bread", "eggs"} #set
p = frozenset({"apple", "banana", "cherry"}) #frozenset
p = True #bool
p = b"hello" #bytes
p = bytearray(5) #bytearray
p = memoryview(bytes(5)) #memoryview
p = None #Nonetype

